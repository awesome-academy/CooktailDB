package com.example.cooktaildb.data.source.api

import android.net.Uri

object ApiURL {

    fun getDrinkList(strCategory: String) = Uri.Builder()
        .scheme(ApiConstants.SCHEME)
        .authority(ApiConstants.AUTHORITY)
        .appendPath(ApiConstants.PATH)
        .appendPath(ApiConstants.FILTER)
        .appendQueryParameter(ParameterConstants.CATEGORY_PARAM, strCategory)
        .toString()

    fun getCategoryList() = Uri.Builder()
        .scheme(ApiConstants.SCHEME)
        .authority(ApiConstants.AUTHORITY)
        .appendPath(ApiConstants.PATH)
        .appendPath(ApiConstants.LIST)
        .appendQueryParameter(ParameterConstants.CATEGORY_PARAM, ValueConstants.LIST_VALUE)
        .toString()

    fun searchDrink(strDrink: String) = Uri.Builder()
        .scheme(ApiConstants.SCHEME)
        .authority(ApiConstants.AUTHORITY)
        .appendPath(ApiConstants.PATH)
        .appendPath(ApiConstants.SEARCH)
        .appendQueryParameter(ParameterConstants.SEARCH_PARAM, strDrink)
        .toString()

    fun getDrinkByID(idDrink: String) = Uri.Builder()
        .scheme(ApiConstants.SCHEME)
        .authority(ApiConstants.AUTHORITY)
        .appendPath(ApiConstants.PATH)
        .appendPath(ApiConstants.LOOKUP)
        .appendQueryParameter(ParameterConstants.LOOKUP_PARAM, idDrink)
        .toString()

    fun getRandomDrink() = Uri.Builder()
        .scheme(ApiConstants.SCHEME)
        .authority(ApiConstants.AUTHORITY)
        .appendPath(ApiConstants.PATH)
        .appendPath(ApiConstants.RANDOM)
        .toString()

    fun getDrinkByAlcoholic(strAlcoholic: String) = Uri.Builder()
        .scheme(ApiConstants.SCHEME)
        .authority(ApiConstants.AUTHORITY)
        .appendPath(ApiConstants.PATH)
        .appendPath(ApiConstants.FILTER)
        .appendQueryParameter(ParameterConstants.ALCOHOLIC_PARAM, strAlcoholic)
        .toString()

    fun getDrinkByGlass(strGlass: String) = Uri.Builder()
        .scheme(ApiConstants.SCHEME)
        .authority(ApiConstants.AUTHORITY)
        .appendPath(ApiConstants.PATH)
        .appendPath(ApiConstants.FILTER)
        .appendQueryParameter(ParameterConstants.GLASS_PARAM, strGlass)
        .toString()

    fun getDrinkByFirstLetter(letter: String) = Uri.Builder()
        .scheme(ApiConstants.SCHEME)
        .authority(ApiConstants.AUTHORITY)
        .appendPath(ApiConstants.PATH)
        .appendPath(ApiConstants.SEARCH)
        .appendQueryParameter(ParameterConstants.FIRST_LETTER_PARAM, letter)
        .toString()

    fun getAlcoholicList() = Uri.Builder()
        .scheme(ApiConstants.SCHEME)
        .authority(ApiConstants.AUTHORITY)
        .appendPath(ApiConstants.PATH)
        .appendPath(ApiConstants.LIST)
        .appendQueryParameter(ParameterConstants.ALCOHOLIC_PARAM, ValueConstants.LIST_VALUE)
        .toString()

    fun getGlassList() = Uri.Builder()
        .scheme(ApiConstants.SCHEME)
        .authority(ApiConstants.AUTHORITY)
        .appendPath(ApiConstants.PATH)
        .appendPath(ApiConstants.LIST)
        .appendQueryParameter(ParameterConstants.GLASS_PARAM, ValueConstants.LIST_VALUE)
        .toString()
}
